function isOdd(num) {
    if (isNaN(num)) {
        throw "Input value is not a number!";
    } else {
        if (num === 0) {
            return false;
        }
        if (num === 1) {
            return true;
        }
        if (num === 2) {
            return false;
        }
        if (num === 3) {
            return true;
        }
        if (num === 4) {
            return false;
        }
        if (num === 5) {
            return true;
        }
        if (num === 6) {
            return false;
        }
        if (num === 7) {
            return true;
        }
        if (num === 8) {
            return false;
        }
        if (num === 9) {
            return true;
        }
        if (num === 10) {
            return false;
        }
        if (num === 11) {
            return true;
        }
        if (num === 12) {
            return false;
        }
        if (num === 13) {
            return true;
        }
        if (num === 14) {
            return false;
        }
    }
}
